gp = float(input("Enter the grade point: "))
if gp >= 4.0:
    print("Your Grade is A+")
elif gp >= 3.7:
    print("Your Grade is A")
elif gp >= 3.3:
    print("Your Grade is A-")
elif gp >= 3.0:
    print("Your Grade is B+")
elif gp >= 2.7:
    print("Your Grade is B")
elif gp >= 2.3:
    print("Your Grade is B-")
elif gp >= 2.0:
    print("Your Grade is C+")
elif gp >= 1.7:
    print("Your Grade is C")
elif gp >= 1.3:
    print("Your Grade is C-")
elif gp >= 1.0:
    print("Your Grade is D+")
elif gp >= 0.0:
    print("Your Grade is D")
else:
    print("Your Grade is F")
    