m = int(input("Enter denomination amount: "))
if m == 1:
    print("George Washington")
elif m == 2:
    print("Thomas Jefferson")
elif m == 5:
    print("Abraham Lincoln")
elif m == 10:
    print("Alexander Hamilton")
elif m == 20:
    print("Andrew Jackson")
elif m == 50:
    print("Ulysses S. Grant")
elif m == 100:
    print("Benjamin Franklin")
else:
    print("Invalid input.")